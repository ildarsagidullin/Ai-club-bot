# ── Карточка мероприятия (читается из БД) ────────────────────────────────────
def build_event_card() -> str:
    """Собирает карточку события из текущих данных в MongoDB."""
    event = db.get_event()
    topic    = esc(event.get("topic", "Скоро"))
    date     = esc(event.get("date", "—"))
    location = esc(event.get("location", "—"))
    map_url  = event.get("map", "")

    if map_url:
        location_str = f"[{location}]({map_url})"
    else:
        location_str = location

    return (
        "🗓 *Ближайшая встреча AI Club Nha Trang*\n\n"
        f"📌 *Тема:* {topic}\n\n"
        f"🕐 *Когда:* {date}\n"
        f"📍 *Где:* {location_str}\n\n"
        "Количество мест ограничено 👇"
    )


